// Q8 Write a Dart program that declares two numeric variables and performs addition, subtraction, multiplication, division, and modulo on them. Print each result.

void main() {
  double salary = 500;
  double salaryYourBr = 400;
  double salaryYourBrDouble;
  salaryYourBrDouble = salary + salaryYourBr;
  print(salaryYourBrDouble);
  salaryYourBrDouble = salary - salaryYourBr;
  print(salaryYourBrDouble);
  salaryYourBrDouble = salary * salaryYourBr;

  print(salaryYourBrDouble);
  salaryYourBrDouble = salary / salaryYourBr;
  print(salaryYourBrDouble);
  salaryYourBrDouble = salary % salaryYourBr;
  print(salaryYourBrDouble);
}
